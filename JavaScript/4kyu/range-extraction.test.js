const solution = require('./range-extraction');

describe('#rangeExtraction', () => {
  const tests = [
    {
      list: [12, 13, 14],
      range: '12-14',
    },
    {
      list: [12, 13, 15, 16, 17],
      range: '12,13,15-17',
    },
    {
      list: [-6, -3, -2, -1, 0, 1, 3, 4, 5, 7, 8, 9, 10, 11, 14, 15, 17, 18, 19, 20],
      range: '-6,-3-1,3-5,7-11,14,15,17-20',
    },
    {
      list: [-96,-95,-92,-90,-87,-85,-83,-82,-80,-79,-76,-74,-72,-69,-68,-67],
      range: '-96,-95,-92,-90,-87,-85,-83,-82,-80,-79,-76,-74,-72,-69--67',
    },
    {
      list: [-69,-67,-64,-61,-59,-58,-56,-55,-53,-52,-50,-49,-48,-47,-44,-42],
      range: '-69,-67,-64,-61,-59,-58,-56,-55,-53,-52,-50--47,-44,-42',
    },
    {
      list: [-68,-65,-64,-63,-61,-58,-56,-54,-51,-49,-48,-47,-46,-45,-44,-43,-42,-41,-40],
      range: '-68,-65--63,-61,-58,-56,-54,-51,-49--40',
    },
  ];
  
  Object.keys(tests).forEach((key) => {
    const { list, range } = tests[key];
  
    it(`test ${key}`, () => {
      Test.expect(solution(list), range);
    });
  });
});
